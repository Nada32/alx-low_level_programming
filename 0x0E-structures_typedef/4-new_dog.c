#include <stdio.h>
#include <stdlib.h>
#include "dog.h"

/**
 * new_dog - new initialize a variable of type struct
 * @d: parameter
 * @name: parameter
 * @age: parameter
 * @owner: parameter
 * Return: Always 0.
 */
dog_t *new_dog(char *name, float age, char *owner)
{
	if (d == NULL)
	;
	else
	{
	d->name = name;
	d->age = age;
	d->owner = owner;
	}
}
